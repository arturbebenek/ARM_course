from qspypy.qspy import FILTER, QS_OBJ_KIND


def on_reset(qutest):
    qutest.glb_filter(FILTER.SM)
    qutest.current_obj(QS_OBJ_KIND.SM,"the_hsm")


def test_QHsmTst_init(qutest):
    qutest.init()
    qutest.expect("===RTC===> St-Init  Obj=the_hsm,State=QHsm_top->QHsmTst_s2")
    qutest.expect("===RTC===> St-Entry Obj=the_hsm,State=QHsmTst_s")
    qutest.expect("===RTC===> St-Entry Obj=the_hsm,State=QHsmTst_s2")
    qutest.expect("===RTC===> St-Init  Obj=the_hsm,State=QHsmTst_s2->QHsmTst_s211")
    qutest.expect("===RTC===> St-Entry Obj=the_hsm,State=QHsmTst_s21")
    qutest.expect("===RTC===> St-Entry Obj=the_hsm,State=QHsmTst_s211")
    qutest.expect("%timestamp Init===> Obj=the_hsm,State=QHsmTst_s211")
    qutest.expect("%timestamp Trg-Done QS_RX_EVENT")

